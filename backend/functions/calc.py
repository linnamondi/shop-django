def square(number):
    answer=(number*number)
    return answer
    


def add(x,y):
    answer=x+y
    return answer


def multiply(k,z):
    answer=k*z
    return answer


def divide(w,q):
    answer=w/q
    return answer


def substract(a,b):
    answer=a-b
    return answer


def exponent(c):
    answer=c**2
    return answer


def modulus(d,j):
    answer =d%j
    return answer

def sum(*numbers):
    total=0
    for number in numbers:
        total+= number
    return total


